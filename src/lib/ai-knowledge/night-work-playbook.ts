// 自由入力の業種名から内部の STORE_TYPE_HINTS キーを推定する
// 設定 UI はユーザー入力の自由テキストだが、プレイブック注入のために
// キーワードマッチで業種種別を推定する（UI に選択肢として出したくない業種も含む）
export function resolveStoreHintKey(storeTypeName: string | null | undefined): StoreType | null {
  if (!storeTypeName) return null;
  const t = storeTypeName.trim().toLowerCase();
  if (!t) return null;

  // 優先度順: より具体的なキーワードを先に判定
  if (/(パパ活|papa)/i.test(t)) return 'papa_katsu';
  if (/(ギャラ飲み|ギャラノミ|ギャラ|pato|パト)/i.test(t)) return 'gyara_nomi';
  if (/(風俗|デリヘル|ソープ|ホテヘル|箱ヘル|ピンサロ|ヘルス|ピンク|派遣型)/i.test(t)) return 'fuzoku';
  if (/(ホスト|host)/i.test(t)) return 'host';
  if (/(キャバ|cabaret)/i.test(t)) return 'cabaret';
  if (/(ラウンジ|lounge)/i.test(t)) return 'lounge';
  if (/(ガールズバー|ガルバ|girls)/i.test(t)) return 'girls_bar';
  if (/(スナック|snack)/i.test(t)) return 'snack';
  if (/(会員制|クラブ|club)/i.test(t)) return 'club';
  if (/(バー|bar|立ち飲み)/i.test(t)) return 'bar';
  return null;
}

// プロンプト注入用のテキストを構築
export function buildPlaybookInstruction(params: {
  storeType?: StoreType | null;
  scene?: string | null;
  compact?: boolean;       // チャット系の短縮版
}): string {
  const parts: string[] = [];

  // VOICE_RULES は常に最上段に注入（禁止クリシェ・改行必須・括弧書き禁止 等）
  parts.push(VOICE_RULES);

  if (params.compact) {
    // チャット用は要点のみ
    parts.push(`
## 夜職営業の要点（常に意識）
- 相手の名前・店内で話した具体内容を必ず混ぜる
- 関係構築 6 要素（特別感・傾聴・感情可視化・記念日・未来予定・ボイス推奨）から最低1つ
- 関係ステージ（S1初回 / S2育成 / S3常連 / S4休眠 / S5離反懸念）に応じて踏み込み度を変える
- 客タイプ（自己中心/等価交換/配慮）で投資配分を変える。等価交換型が中心層
- 返信を強要しない、「思い出してもらう」が目的
- 他キャスト比較・元パートナー深掘り・プライド傷つけ は絶対NG
- 同伴誘いは予定確認 → 具体提案の順、押し付けない
- 既読スルーは関係継続のサイン、追い LINE は NG
- 具体日程を返されたら即レス、泥酔深夜は短く温かく、営業誘導しない
- キャスト個人の一人称・語尾・絵文字頻度を最優先、過度な甘え語尾 NG
`.trim());
  } else {
    parts.push(NIGHT_WORK_BASE_PLAYBOOK);
  }

  if (params.storeType && STORE_TYPE_HINTS[params.storeType]) {
    parts.push(STORE_TYPE_HINTS[params.storeType]!);
  }

  if (params.scene && SCENE_ADDITIONAL_HINTS[params.scene]) {
    parts.push(SCENE_ADDITIONAL_HINTS[params.scene]);
  }

  return parts.join('\n\n');
}
